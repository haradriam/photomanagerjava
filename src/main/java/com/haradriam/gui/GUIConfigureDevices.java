    /**
     * formWindowOpened: Init list of devices
     * @param evt 
     */
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultListModel listModel = new DefaultListModel();
        DeviceManager dm = new DeviceManager();
        String[] deviceName;
        
        try {
            deviceName = dm.getAvailableDevices();
            for(int i = 0; i < deviceName.length; i++) { //Read the available devices
                listModel.addElement(deviceName[i]); //Store each available device
            }
        } catch (Exception ex) {
            Logger.getLogger(GUIConfigureDevices.class.getName()).log(Level.SEVERE, null, ex);
        }

        lstDevices.setModel(listModel);
    }//GEN-LAST:event_formWindowOpened

