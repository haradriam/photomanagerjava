    /**
     * btnDelActionPerformed: Delete item from the list of devices
     * @param evt 
     */
    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        DefaultListModel listModel;
        
        listModel = (DefaultListModel) lstDevices.getModel();
        listModel.removeElementAt(lstDevices.getSelectedIndex());
        
        lstDevices.setModel(listModel);
    }//GEN-LAST:event_btnDelActionPerformed

    /**
     * btnAcceptActionPerformed: Save list of devices
     * @param evt 
     */
    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        DefaultListModel listModel;
        DeviceManager dm = new DeviceManager();
        String[] deviceName;
        List<String> list = new ArrayList<>();
        
        listModel = (DefaultListModel) lstDevices.getModel();
        
        for(int i = 0; i < listModel.getSize(); i++) {
            list.add(listModel.getElementAt(i).toString());
        }
        
        deviceName = list.toArray(new String[list.size()]);
        
        try {
            dm.setAvailableDevices(deviceName);
            System.exit(0);
        } catch (Exception ex) {
            Logger.getLogger(GUIConfigureDevices.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

